	AREA RESET,CODE
	LDR R0,=LIST
	LDR R1,=RESULT
	LDR R2,=NUM
	LDR R3,=COUNT
	MOV R4,#0
FIND LDR R5,[R0],#4
	CMP R5,R2
	BNE SKIP
	ADD R4,R4,#1
SKIP SUBS R3,R3,#1
	BNE FIND
	STR R4,[R1]
STOP B STOP
LIST DCD 0,5,1,4,100,4,0,8,7,20
NUM EQU 0
COUNT EQU 9
	AREA MEMORY,DATA
RESULT SPACE 4
	END