	AREA RESET,CODE
	LDR R0,=LIST
	LDR R5,=RESULT
	LDR R1,[R0] ;MIN
	LDR R2,[R0],#04 ;MAX
	MOV R4,#4
REPEAT	LDR R3,[R0],#04
	CMP R1,R3
	BLE LESS
	MOV R1,R3
LESS CMP R2,R3 
	BGE MORE
	MOV R2,R3
MORE SUBS R4,R4,#1
	BNE REPEAT
	STR R1,[R5],#4
	STR R2,[R5]
STOP B STOP
LIST DCD &64,&05,&96,&10,&65
	AREA MEMORY,DATA
RESULT SPACE 8
	END